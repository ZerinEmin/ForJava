package introduction;

public class IntegerTypes {

	public static void main(String[] args) {
		byte xyz = 100;
		System.out.println(xyz);
		
		short shortDegiskeni = 1000;
		System.out.println("short degeri :" + shortDegiskeni);
		
		int intDegeri = 100000;
		System.out.println("integer degeri :" + intDegeri);
		
		long longDegeri = 1000000000;
		System.out.println("long degeri :" + longDegeri);
	}

}
