package exerciseprojects;

import java.util.Scanner;

public class VowelPresence {
	static void Vowel_Or_Consonant(char y) 
	
    { 
		
        if (y == 'a' || y == 'e' || y == 'i' || y == 'o'
            || y == 'u') 
            System.out.println("It is a Vowel."); 
        else
            System.out.println("It is a Consonant."); 
    } 

}
